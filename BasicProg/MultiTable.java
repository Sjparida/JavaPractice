package BasicProg;

public class MultiTable {
    public static void main(String[] args)
    {
        System.out.println("---------------------");
        System.out.println("Multiplication Table");
        System.out.println("---------------------");
        for(int i=1;i<=10;i++)
        {
            int m = 4*i;
            System.out.println("4 * " + i + " = " + m);
        }
    }
}
